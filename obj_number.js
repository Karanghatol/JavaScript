/*
    * In JavaScript, the Number object is a wrapper object that represents numerical values.
    * Similar to the Boolean object, the Number object is rarely used in practice, 
    * as most numeric operations are performed directly with primitive number values.
    * You can create a Number object using the Number constructor.
*/

//     Here's an example:
{
    let num1 = new Number(10)
    console.log(typeof(num1));
    console.log(num1.valueOf());
}